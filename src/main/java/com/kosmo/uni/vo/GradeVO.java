package com.kosmo.uni.vo;

public class GradeVO {

	private String id;
	private String name;
	private String co_code;
	private String grades_code;
	private float attend;
	private float midterm;
	private float finals;
	private float assign_report;
	private float assign_team;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCo_code() {
		return co_code;
	}
	public void setCo_code(String co_code) {
		this.co_code = co_code;
	}
	public String getGrades_code() {
		return grades_code;
	}
	public void setGrades_code(String grades_code) {
		this.grades_code = grades_code;
	}
	public float getAttend() {
		return attend;
	}
	public void setAttend(float attend) {
		this.attend = attend;
	}
	public float getMidterm() {
		return midterm;
	}
	public void setMidterm(float midterm) {
		this.midterm = midterm;
	}
	public float getFinals() {
		return finals;
	}
	public void setFinals(float finals) {
		this.finals = finals;
	}
	public float getAssign_report() {
		return assign_report;
	}
	public void setAssign_report(float assign_report) {
		this.assign_report = assign_report;
	}
	public float getAssign_team() {
		return assign_team;
	}
	public void setAssign_team(float assign_team) {
		this.assign_team = assign_team;
	}
	
	

}
