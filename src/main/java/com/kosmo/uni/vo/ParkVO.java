package com.kosmo.uni.vo;

public class ParkVO {

	private int num;
	private String id;
	private String pintime; 
	private String pouttime;
	private String carNum;
	private String name;
	private String depart_name;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPintime() {
		return pintime;
	}
	public void setPintime(String pintime) {
		this.pintime = pintime;
	}
	public String getPouttime() {
		return pouttime;
	}
	public void setPouttime(String pouttime) {
		this.pouttime = pouttime;
	}
	public String getCarNum() {
		return carNum;
	}
	public void setCarNum(String carNum) {
		this.carNum = carNum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDepart_name() {
		return depart_name;
	}
	public void setDepart_name(String depart_name) {
		this.depart_name = depart_name;
	}
	
	
	
}
